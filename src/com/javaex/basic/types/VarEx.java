package com.javaex.basic.types;

public class VarEx {
	public static void main(String[] args) {
		int myAge1 = 52;
		System.out.println("내 나이는 " + myAge1 + "살 이다");
		int v1 = 10, v2 = 20, v3 = 30;
		System.out.println(v1);
		System.out.println(v2);
		System.out.println(v3); 
		
		v1 = v2 = v3 = 2024;
		
		System.out.println(v1);
		System.out.println(v2);
		System.out.println(v3);
	}
}
